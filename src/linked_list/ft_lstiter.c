#include "libft.h"

/**
 * @brief Iterates the list 'lst' and applies the function 'f' to the content of
 * each node.
 * 
 * @param lst A pointer to the list.
 * @param f The function used to iterate on the list.
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}
