#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new;
	size_t	i;
	size_t	len;

	len = ft_strlen(s);
	new = (char *)malloc((len + 1) * sizeof(char));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
