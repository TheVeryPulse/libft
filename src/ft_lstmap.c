#include "libft.h"

/**
 * @brief Iterates the list 'lst' and applies the function 'f' to the content of
 * each node.
 * 
 * Creates a new list resulting from the successive applications of the function
 * 'f'. The 'del' function is used to delete the content of a node if needed.
 * 
 * @param lst A pointer to the list.
 * @param f The function used to iterate on the list.
 * @param del The function used to delete the content of a node if needed.
 * 
 * @return The new list. NULL if the allocation fails.
 */
t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*new_node;

	if (lst == NULL || f == NULL || del == NULL)
		return (NULL);
	head = NULL;
	while (lst)
	{
		new_node = ft_lstnew(f(lst->content));
		if (!new_node)
		{
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, new_node);
		lst = lst->next;
	}
	return (head);
}
